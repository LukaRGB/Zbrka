    <h1 class="flow-text">Nešto o našoj zemlji</h1>
    <p class="flow-text">
    Srbija, ili zvanično Republika Srbija, kontinentalna je država koja se nalazi u jugoistočnoj Evropi a prostire se na Balkanskom poluostrvu i jednim delom u srednjoj Evropi (Panonskoj niziji). Glavni grad je Beograd. Sa 1.639.121 stanovnika u široj okolini po popisu iz 2011. godine, on je administrativno i ekonomsko središte države. Srbija se na severu graniči sa Mađarskom, na severoistoku sa Rumunijom, na istoku sa Bugarskom, na jugu sa Republikom Makedonijom, na jugozapadu sa Albanijom i Crnom Gorom a na zapadu sa Hrvatskom i Bosnom i Hercegovinom (Republika Srpska).<br>


        Zanimljive činjenice: <br>

    <b>1. Maline</b><br>
    Srbija je zemlja koja je poslednjih godina najveći izvoznik malina u svetu. 2012. godine je čak 95% malina bilo poreklom iz Srbije.<br>

    <b>2. Rimski imperatori</b><br>
    Tokom III i IV veka, 18 rimskih imperatora je rođeno na teritoriji današnje Srbije. To je petina od njihovog ukupnog broja.<br>

    <b>3. Vampir</b><br>
    Jedina srpska reč koja je prihvaćena i odomaćena širom sveta je vampir.<br>

    <b>4. Satovi</b><br>
    Proizvodnja satova u Srbiji je starija nego u Švajcarskoj. Srpski monah Lazar Svetogorac je napravio prvi javni mehanički sat čak 200 godina pre nego što su Švajcaraci osnovali prvi esnaf.<br>

    <b>5. Crkva</b><br>
    Gradska crkva u Čačku je tokom svoje istorije čak deset puta, pod najezdom Turaka, pretvarana u džamiju.<br>

    <b>6. Video prenos</b><br>
    Prvi satelitski video prenos između Severne Amerike i Evrope se odigrao 1963. godine i prikazao je fresku Belog Anđela iz Manastira Mileševa.<br>

    <b>7. Zvanični jezik u Turskom carstvu</b><br>
    Jedan od četiri zvanična jezika u Turskom carstvu bio je srpski.<br>

    <b>8. Beograd</b><br>
    Prestonica Srbije, Beograd, je jedan od najvećih gradova jugoistočne Evrope. Takođe, prestižni turistički portal Lonely Planet je proglasio Beograd za grad sa najboljim noćnim životom na svetu.<br>

    <b>9. Klisura</b><br>
    Đerdapska klisura, najveća klisura u Evropi kroz koju protiče Dunav, nalazi se u Srbiji.<br>

    <b>10. Kanjon reke Temštice</b><br>
    Neobične crvene stene impresivnog kanjona reke Temštice neodoljivo će vas podsetiti na čuveni Kolorado!<br>

    <b>11. Prašuma Vinatovača</b><br>
    Srbija takođe ima jednu prašumu. Prašuma Vinatovača je specijalni rezervat prirode u kojem drveće bukve raste već preko 350 godina. Ovde je seča drveća, branje biljaka, pa čak i pomeranje palih debala zabranjeno.<br>

    <b>12. Plutajuća ostrva</b><br>
    E pa, u Srbiji i tako nešto postoji! Na Semeteškom i Vlasinskom jezeru ostrva plutaju.

    <b>13. Đavolja Varoš</b><br>
    Na jugu Srbije, blizu Kuršumlije, nalazi se jedno od 8 srpskih čuda prirode – Đavolja varoš. Ova atrakcija je svetski poznata zbog svojih 202 neobične zemljane figure nastale erozijom zemlje.<br>

    <b>14. Hram Svetog Save</b><br>
    Hram Svetog Save ne samo da je najveća srpska pravoslavna crkva već je i najveći pravoslavni pravoslavni hram na Balkanu i jedan od najvećih na svetu.<br>

    <b>15. Gostoprimstvo</b><br>
    Srbi su, po statistikama, najgostoljubiviji narod na svetu.<br>
