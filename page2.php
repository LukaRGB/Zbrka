    <h3 class="flow-text">Cilj otvaranja muzeja Narodnog pozorista u Beogradu</h3>
    <p class="flow-text">Dok se gradila zgrada Narodnog pozorišta na zemljištu kod Stambol kapije, u nedelju 22. novembra 1868. godine, na drugoj lokaciji, izvedena je prva predstava; istorijska drama u pet razdela – Đurađ Branković Karla Obernjika u prevodu i preradi tek postavljenog upravnika Jovana Đorđevića. Izvedena je u adaptiranoj sali gostionice Kod engleske kraljice, u kući koju je Pozorišni odbor zakupio od sveštenika Miloša Sušića, paroha Saborne crkve, a nalazila se u nekadašnjoj Kosmajskoj ulici broj 51 (danas ulica Maršala Birjuzova). Pre predstave izvedena je Srpska uvertira kompozitora Dragutina Reša, a uvodnu reč o pozorišnoj umetnosti, dramskoj književnosti i dotadašnjim dramskim predstavama kod Srba, održao je književnik Milorad Popović Šapčanin, potonji predsednik srpske vlade. Sušićeva kuća, koja se nalazila u neposrednoj bilizini tadašnje Varoš kapije, danas ne postoji. Ali, o njoj ne postoji ni kolektivno sećanje i ako je ona mesto nastanka jedne od najznačajnijih kulturnih ustanova srpskog naroda. U njoj će glumci Narodnog pozorišta do kraja maja naredne 1869. godine odigrati čak sedamdeset predstava, da bi novu pozorišnu sezonu otvorili u novoj zgradi, u četvrtak 30. oktobra izvođenjem slike iz narodnog života s pevanjem –  Posmrtna slava kneza Mihaila Đorđa Maletića, uz muziku pozorišnog kapelnika Dragutina Reša. U kamenom zdanju kod nekadašnje Stambol kapije, u zgradi kod Spomenika, na Pozorišnom trgu, odnosno na Trgu Republike, skoro vek i po, s manjim prekidima zbog ratova i ratnih razaranja, izvode se predstave: od početka dramske, od 1920. operske, a od 1923. godine i baletske celovečernje.</p>
